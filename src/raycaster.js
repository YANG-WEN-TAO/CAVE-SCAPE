/**
 * 初始化射线拾取系统
 */
export function initRaycaster() {
  raycaster = new THREE.Raycaster()
  mouse = new THREE.Vector2()
  console.log('[射线拾取] Raycaster 初始化完成')
}

/**
 * 绑定射线拾取到场景
 * @param {THREE.Camera} cam - 相机
 * @param {HTMLElement} dom - 渲染 DOM 元素
 * @param {THREE.Group} mdl - 加载的模型
 */
export function bindRaycastToScene(cam, dom, mdl) {
  camera = cam
  domElement = dom
  model = mdl

  if (!raycaster) initRaycaster()

  // 鼠标点击事件
  domElement.addEventListener('click', onPointerClick)

  // 鼠标移动事件（悬停高亮）
  domElement.addEventListener('pointermove', onPointerMove)

  console.log('[射线拾取] 已绑定到场景')
}

/**
 * 鼠标点击处理
 */
function onPointerClick(event) {
  if (!camera || !model) return

  // 计算鼠标 NDC 坐标（-1 到 1）
  mouse.x = (event.clientX / window.innerWidth) * 2 - 1
  mouse.y = -(event.clientY / window.innerHeight) * 2 + 1

  // 设置射线
  raycaster.setFromCamera(mouse, camera)

  // 检测相交对象
  const intersects = raycaster.intersectObject(model, true)

  if (intersects.length > 0) {
    // 找到第一个可交互对象
    let target = null
    for (const intersect of intersects) {
      let obj = intersect.object
      // 向上查找有名称的对象
      while (obj && !target) {
        if (obj.name && isInteractiveName(obj.name)) {
          target = obj
          break
        }
        obj = obj.parent
      }
      if (target) break
    }

    if (target) {
      showArtifactInfo(target.name)
    }
  }
}

/**
 * 鼠标移动处理（悬停高亮）
 */
let hoveredObject = null

function onPointerMove(event) {
  if (!camera || !model) return

  mouse.x = (event.clientX / window.innerWidth) * 2 - 1
  mouse.y = -(event.clientY / window.innerHeight) * 2 + 1

  raycaster.setFromCamera(mouse, camera)
  const intersects = raycaster.intersectObject(model, true)

  // 恢复之前悬停对象
  if (hoveredObject) {
    hoveredObject.material.emissiveIntensity = 0
    hoveredObject = null
    domElement.style.cursor = 'default'
  }

  if (intersects.length > 0) {
    let target = null
    for (const intersect of intersects) {
      let obj = intersect.object
      while (obj && !target) {
        if (obj.name && isInteractiveName(obj.name)) {
          target = obj
          break
        }
        obj = obj.parent
      }
      if (target) break
    }

    if (target && target.material) {
      hoveredObject = target
      // 高亮效果
      if (target.material.emissive) {
        target.material.emissiveIntensity = 0.3
      }
      domElement.style.cursor = 'pointer'
    }
  }
}

/**
 * 判断名称是否为可交互对象
 */
function isInteractiveName(name) {
  const keywords = ['壁画', '经变画', '飞天', '伎乐', '供养人', '护法', '藻井', '千佛',
    '彩塑', '主佛', '迦叶', '阿难', '弟子', '菩萨', '天王', '力士', '洞窟', '佛龛', '门洞',
    '市井', '崖壁', '九层楼', '密宗画', '密宗千佛', '密宗胁侍', '密宗供养', '密宗主尊',
    '密宗藻井', '密宗经变画', '绢画', '僧人', '千手观音', '西魏佛', '洪辩', '胁侍']
  return keywords.some(kw => name.includes(kw))
}

/**
 * 显示文物解说弹窗
 * 匹配策略：按 key 长度降序匹配，长键优先，确保特定壁画不会误匹配到通用 key
 */
function showArtifactInfo(objectName) {
  // 按 key 长度降序排列，长键优先匹配
  const sortedKeys = Object.keys(ARTIFACT_INFO).sort((a, b) => b.length - a.length)

  let info = null
  for (const key of sortedKeys) {
    if (objectName.includes(key)) {
      info = ARTIFACT_INFO[key]
      break
    }
  }

  if (!info) {
    console.log('[射线拾取] 未找到文物信息: ' + objectName)
    return
  }

  // 显示弹窗
  const panel = document.getElementById('info-panel')
  const title = document.getElementById('info-title')
  const content = document.getElementById('info-content')

  if (panel && title && content) {
    title.textContent = info.title
    content.innerHTML =
      '<p><strong>朝代：</strong>' + info.dynasty + '</p>' +
      '<p>' + info.content + '</p>'
    panel.style.display = 'block'
  }

  console.log('[射线拾取] 点击: ' + objectName + ' -> ' + info.title)
}
